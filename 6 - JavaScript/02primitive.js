// Primitive Data Types

// String textual data enclosed in "" or ''
let firstName = "John";
console.log("firstName:", firstName);
console.log("Data Types:", typeof firstName);


// Number: numerical value(30, -31, 30.20)
let age = 30;
console.log("Age", age);
console.log("Data Type:", typeof age);

// boolean true or false
let isStudent = false;
console.log("Is Student:", isStudent);
console.log("Data Type:", typeof isStudent);

// null Intentional absence of value 
let car = null;
console.log("Car:", car);
console.log("Data Type:", typeof car);

car = "Honda";
console.log("Car:", car);
console.log("Data Type:", typeof car);

// Undefined: Unintentional absence of value
let city;
console.log("City:", city);
console.log("Data TYpe:", typeof city);